// Do While : It is same as wile loop but it will execute atleast once with 'do' condition
// First it execute the statement and then verify condition

// Ex1: Print numbers in ascending order
console.log("****** numbers in ascending order ******");
let num = 1;
do{
    console.log(num);
    num++;
}while(num <= 5);

// Ex2: Print numbers in descending order
console.log("****** numbers in descending order ******");
let num1 = 10;
do{
    console.log(num1);
    num1--;
}while(num1 >= 1);